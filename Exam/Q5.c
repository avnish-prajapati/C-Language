//Print a below pattern using nested for loop in C language:
//100 
// -19
//81 81
// -17
//64 64 64
// -15
//49 49 49 49
// -13
//36 36 36 36 36
void main ()
// even number 19,17,15,13....
{
	int i,j;
	
	for(i=1; i<=100; i++) // outer loop 
	{
		for(j=1; j<i; j++)  // inner loop 
		{
			printf("%d ",i);
			
		if(i%=0)
		{
			printf("%d ",i);
		}
		else{
			
			}	
		}
		printf("\n");
	}
	
	
	
	
	
	
	
}
